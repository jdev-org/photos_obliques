package org.georchestra.photooblique.exception;

public class InputAttributException extends Exception {

	/**
	* 
	*/
	private static final long serialVersionUID = 182741205597673503L;

	public InputAttributException() {
		super();
	}

	public InputAttributException(String message) {
		super(message);
	}

	public InputAttributException(String message, Throwable cause) {
		super(message, cause);
	}

	public InputAttributException(Throwable cause) {
		super(cause);
	}

}
