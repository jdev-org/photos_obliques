package org.georchestra.photooblique.exception;

public class CityCodeFormatException extends Exception {

	/**
	* 
	*/
	private static final long serialVersionUID = 182741205597673503L;

	public CityCodeFormatException() {
		super();
	}

	public CityCodeFormatException(String message) {
		super(message);
	}

	public CityCodeFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public CityCodeFormatException(Throwable cause) {
		super(cause);
	}

}
